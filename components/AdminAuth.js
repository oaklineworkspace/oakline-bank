import { useState, useEffect } from 'react';
import { useRouter } from 'next/router';

const ADMIN_PASSWORD = 'Chrismorgan23$';

export default function AdminAuth({ children }) {
  const [isAuthenticated, setIsAuthenticated] = useState(false);
  const [password, setPassword] = useState('');
  const [error, setError] = useState('');
  const router = useRouter();

  useEffect(() => {
    const adminAuth = localStorage.getItem('adminAuthenticated');
    if (adminAuth === 'true') {
      setIsAuthenticated(true);
    }
  }, []);

  const handleLogin = (e) => {
    e.preventDefault();
    if (password === ADMIN_PASSWORD) {
      setIsAuthenticated(true);
      localStorage.setItem('adminAuthenticated', 'true');
      setError('');
    } else {
      setError('Invalid password');
    }
  };

  const handleLogout = () => {
    setIsAuthenticated(false);
    localStorage.removeItem('adminAuthenticated');
    setPassword('');
  };

  if (!isAuthenticated) {
    return (
      <div style={styles.loginContainer}>
        <div style={styles.loginCard}>
          <h1 style={styles.title}>üè¶ Admin Access Required</h1>
          <p style={styles.subtitle}>Enter password to access admin area</p>
          <form onSubmit={handleLogin} style={styles.form}>
            <div style={styles.inputGroup}>
              <label style={styles.label}>Admin Password</label>
              <input
                type="password"
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                style={styles.input}
                placeholder="Enter admin password"
                required
              />
            </div>
            {error && <div style={styles.error}>{error}</div>}
            <button type="submit" style={styles.loginButton}>
              üîê Access Admin Area
            </button>
          </form>
        </div>
      </div>
    );
  }

  return (
    <div>
      <div style={styles.logoutBar}>
        <span style={styles.adminLabel}>üë§ Admin Mode</span>
        <button onClick={handleLogout} style={styles.logoutButton}>
          üö™ Logout
        </button>
      </div>
      {children}
    </div>
  );
}

